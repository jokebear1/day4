import java.util.Scanner;

public class exit {
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		
		System.out.println("exit를 입력하면 종료합니당");
		
		while(true) {
			System.out.print(">>");
		String a=sc.nextLine();
		if(a.equals("exit"))
			break;
				
	}
		System.out.println("종료합니다");
			
	}
		
	}


